let currentLanguage = 'fr';

export function availableLanguages() {
  return ['fr', 'en', 'es', 'pt-BR'];
}

export function setLanguage(language) {
  currentLanguage = translations[language] ? language : 'fr';
  document.documentElement.lang = currentLanguage;
  document.documentElement.dir = 'ltr';
  document.title = t('app.title');
  for (const element of document.querySelectorAll('[data-i18n]')) {
    element.textContent = t(element.dataset.i18n);
  }
  for (const element of document.querySelectorAll('[data-i18n-title]')) {
    element.title = t(element.dataset.i18nTitle);
  }
  return currentLanguage;
}

export function getLanguage() {
  return currentLanguage;
}

export function t(key, values = {}) {
  const template = translations[currentLanguage]?.[key] ?? translations.fr[key] ?? key;
  return String(template).replace(/\{(\w+)\}/g, (_match, name) => String(values[name] ?? `{${name}}`));
}
